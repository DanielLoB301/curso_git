#Función suma
def Suma(num1, num2):
    return num1 + num2

#Función resta
def Resta(num1, num2):
    return num1 - num2

#Función multiplicación
def Multiplicacion(num1, num2):
    return num1 * num2

#Función división
def Division(num1, num2):
    return num1 / num2

#Función raiz cuadrada
def RaizCuadrada(num1, num2):
    return pow(num1,num2)